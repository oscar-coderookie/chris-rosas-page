
const pagina1 = "Edwin Rosa Vázquez (Río Piedras, Puerto Rico; 15 de octubre de 1981), mejor conocido como Ñengo Flow, es un cantante puertorriqueño de reggaetón y trap latino. Nació en Río Piedras, barrio de San Juan y creció en Bayamón, donde tendría su inicio en la música. Fue allí que desde muy joven se desarrollaría el interés por el mundo de la música urbana. Su contenido lírico fuerte y explícito vino de experiencias de su infancia en su barrio. Cuando tenía 14 años fue presentado por primera vez en el escenario en el barrio donde vivía, momento en que descubrió su talento para la música.Su primera cancion fue «Dale Dile Boster»."

const pagina2 = "Después de unos años el nombre de Ñengo Flow se dio a conocer, sus inicios como artista se dieron por la publicación de un mixtape/demo donde había participaciones de Kendo, Guayo Man, Los Sabios, Apocalypse & DNA, que llegó a ser escuchado por la cadena Univisión. Esto le permitió publicar fácilmente su primer álbum de estudio titulado «Flow callejero» en 2005. En 2011, lanzó su segundo álbum, titulado Real G4 Life y en 2012 lanzó dos álbumes secuelas titulados Real G4 Life 2 y Real G4 Life 2.5, los cuales alcanzaron los puesto número 75 y 61 en el Billboard Top Latin Albums. Sus letras explícitas y fuertes le han hecho ganar cierta fama."

